/** Приветствие по часу (0–23), без запятой — для склейки с именем в H1. */
export function dashboardGreetingForHour(lang: Lang, hour: number): string {
  if (hour < 12) return t(lang, "dashboard.greetMorning");
  if (hour < 18) return t(lang, "dashboard.greetAfternoon");
  return t(lang, "dashboard.greetEvening");
}
